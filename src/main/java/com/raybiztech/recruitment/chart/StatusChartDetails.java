package com.raybiztech.recruitment.chart;

public class StatusChartDetails {
	private String caption;
	private String subcaption;
	private String plottooltext;
	private String theme;
	private String xAxisName;
    private String yAxisName;
    private String labelDisplay;
    private String slantLabel;
    
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getSubcaption() {
		return subcaption;
	}
	public void setSubcaption(String subcaption) {
		this.subcaption = subcaption;
	}
	public String getPlottooltext() {
		return plottooltext;
	}
	public void setPlottooltext(String plottooltext) {
		this.plottooltext = plottooltext;
	}
	public String getTheme() {
		return theme;
	}
	public void setTheme(String theme) {
		this.theme = theme;
	}
	public String getxAxisName() {
		return xAxisName;
	}
	public void setxAxisName(String xAxisName) {
		this.xAxisName = xAxisName;
	}
	public String getyAxisName() {
		return yAxisName;
	}
	public void setyAxisName(String yAxisName) {
		this.yAxisName = yAxisName;
	}
	public String getLabelDisplay() {
		return labelDisplay;
	}
	public void setLabelDisplay(String labelDisplay) {
		this.labelDisplay = labelDisplay;
	}
	public String getSlantLabel() {
		return slantLabel;
	}
	public void setSlantLabel(String slantLabel) {
		this.slantLabel = slantLabel;
	}
	
	
	
}
