package com.raybiztech.hiveworkpackages.dto;

public class spentTimeDTO {
	
	private Long projectId;
	private Long userId;
	private Long workPackageId;
	private Float spentTime;
	private String comments;
	private Long activityId;
	private Long year;
	private Long week;
	private String spentDate;
	private Long month;
	private Long versionId;
	
	
	public Long getProjectId() {
		return projectId;
	}
	public void setProjectId(Long projectId) {
		this.projectId = projectId;
	}
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public Long getWorkPackageId() {
		return workPackageId;
	}
	public void setWorkPackageId(Long workPackageId) {
		this.workPackageId = workPackageId;
	}
	public Float getSpentTime() {
		return spentTime;
	}
	public void setSpentTime(Float spentTime) {
		this.spentTime = spentTime;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public Long getActivityId() {
		return activityId;
	}
	public void setActivityId(Long activityId) {
		this.activityId = activityId;
	}
	public Long getYear() {
		return year;
	}
	public void setYear(Long year) {
		this.year = year;
	}
	public Long getWeek() {
		return week;
	}
	public void setWeek(Long week) {
		this.week = week;
	}
	public String getSpentDate() {
		return spentDate;
	}
	public void setSpentDate(String spentDate) {
		this.spentDate = spentDate;
	}
	public Long getMonth() {
		return month;
	}
	public void setMonth(Long month) {
		this.month = month;
	}
	public Long getVersionId() {
		return versionId;
	}
	public void setVersionId(Long versionId) {
		this.versionId = versionId;
	}
	
	

}
